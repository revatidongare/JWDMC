													<p>Start the day with 8am pickup at your hotel in Krabi, then travel to Khlong Thom National Park in a comfortable minivan, passing through a scenic rainforest landscape. Upon arrival, your driver will lead you to the entrance of the natural hot springs, where you can soak, swim, and walk through the nearby forest trails. When you’ve had enough of the natural heat, cool off in the nearby Crystal Pool, also called the Emerald Pool, a spring-fed swimming spot with beautifully clear water. If you’ve selected the half-day trip while booking, you’ll return to Krabi after your time at Crystal Pool; otherwise, refuel for an afternoon of exploring with lunch at a nearby Thai restaurant (included in the price of the full-day tour). Continue to the Tiger Cave Temple, a meditation center for Buddhist monks surrounded by a network of caves. You’ll have the option to climb the 1,237 steps to the summit of the temple, where you’ll find soaring views to the Andaman Sea, Buddha shrines, and altars. This 7-hour tour concludes with return transfer to your Krabi hotel. Read more about Krabi Jungle Tour Including Tiger Cave Temple, Crystal Pool and Krabi Hot Spring.</p>
